package com.aulaficr;

public class Calculadora {
    void soma(float a, float b) {
        System.out.println("Resultado = " + (a+b));
    }
    void subtracao(float a, float b) {
        System.out.println("Resultado = " + (a-b));
    }
    void multiplicacao(float a, float b) {
        System.out.println("Resultado = " + (a*b));
    }
    void divisao(float a, float b) {
        System.out.println("Resultado = " + (a/b));
    }
}
